  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    var that = this;
    var id = options.id;
    app.ajax.req('web/article_detail', {article_id:id}, function (res) {
      
      if (res.code == 0) {
        var content = Base64.decode(res.content)
        WxParse.wxParse('content', 'html', content, that, 5);
      }
    })
    
  },

